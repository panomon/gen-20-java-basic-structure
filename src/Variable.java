public class Variable {
    public static void main (String[] args) {
        String nama = "Ari";
        int usia = 18;
        char kelas = 'A';
        float nilai = 98.76f;
        System.out.println("Nama : " + nama);
        System.out.println("Usia : " + usia);
        System.out.println("Kelas : " + kelas);
        System.out.println("Nilai : " + nilai);
    }
}
